public class _004ArithmeticOp {
    public static void main(String[] args) {
        int x=39 , y=10;
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
        System.out.println(x/y);
        System.out.println(x%y);//it gives remainder

        double a=39 , b=10;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}
